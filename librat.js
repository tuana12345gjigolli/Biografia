function showHome() {
  document.getElementById('booksGrid').innerHTML = '';
  document.getElementById('quote').innerText = '';
}

function showCategory(category) {
  const booksGrid = document.getElementById('booksGrid');
  const quote = document.getElementById('quote');
  booksGrid.innerHTML = '';
  quote.innerText = '';

  booksData[category].forEach(book => {
    const card = document.createElement('div');
    card.className = 'book-card';

    const isLocalImg = !book.img.startsWith('http') && !book.img.startsWith('https');

    card.innerHTML = `<img src="${book.img}" alt="${book.title}">
                      <h3 style="${isLocalImg ? 'letter-spacing: 2px;' : ''}">${book.title}</h3>`;

    card.addEventListener('click', () => {
      quote.innerText = `"${book.quote}"`;
    });

    booksGrid.appendChild(card);
  });
}

function showAllBooks() {
  const booksGrid = document.getElementById('booksGrid');
  const quote = document.getElementById('quote');
  booksGrid.innerHTML = '';
  quote.innerText = '';

  Object.keys(booksData).forEach(category => {
    // Shto titullin e kategorisë me stil
    const categoryTitle = document.createElement('h2');
    categoryTitle.innerText = category.charAt(0).toUpperCase() + category.slice(1);
    categoryTitle.style.color = '#a97c50';
    categoryTitle.style.marginTop = '30px';
    categoryTitle.style.marginBottom = '15px';
    booksGrid.appendChild(categoryTitle);

    // Krijo një container (div) për librat e asaj kategorie që të jenë në grup të veçantë dhe me rrjet (grid)
    const categoryContainer = document.createElement('div');
    categoryContainer.className = 'books-category-container';
    booksGrid.appendChild(categoryContainer);

    booksData[category].forEach(book => {
      const card = document.createElement('div');
      card.className = 'book-card';

      const isLocalImg = !book.img.startsWith('http') && !book.img.startsWith('https');

      card.innerHTML = `<img src="${book.img}" alt="${book.title}">
                        <h3 style="${isLocalImg ? 'letter-spacing: 2px;' : ''}">${book.title}</h3>`;

      card.addEventListener('click', () => {
        quote.innerText = `"${book.quote}"`;
      });

      categoryContainer.appendChild(card);
    });
  });
}
